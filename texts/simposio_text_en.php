<h4>II. Objetivos</h4>

El impacto de la Informática y de las Ciencias de la Computación es tanto científico como social. Más aun, existe una tendencia, tanto a nivel nacional como internacional, que apunta a la generación de conciencia sobre la necesidad de planear y conducir la investigación de manera tal que cause un alto y positivo impacto social. Dicha tendencia, además de ser un imperativo ético, es también una realidad a la que los investigadores deben adaptarse si desean lograr financiación por parte de las agencias gubernamentales o de los sectores productivos. En concordancia con lo anterior, el Simposio de Maestría y Doctorado apunta al cumplimiento de los siguientes objetivos:
<ul>
<li>Contribuir a la construcción y consolidación de una comunidad de estudiantes y profesores de Maestría y Doctorado en el área de Informática y Computación que trabaje en forma colaborativa para contribuir al desarrollo sostenido de Colombia.</li><li>Generar un espacio para que los estudiantes de Maestría o Doctorado puedan presentar sus proyectos de investigación en Informática o Computación, exponiendo sus avances o resultados finales, y haciendo énfasis en la manera en la cual sus proyectos brinda solución a problemáticas, tanto a nivel nacional como internacional.
</li>
</ul>


<h4>III. Estructura del Simposio</h4>

El Simposio se realizará en la tarde del <span class="tutTitle">lunes 21 de Septiembre</span> dentro del programa oficial del 10CCC. El Simposio contempla dos categorías –Maestría o Doctorado– en una actividad principal:
<ul>
<li>Presentación y discusión oral de los proyectos de investigación por parte de los estudiantes: Se seleccionarán hasta 10 trabajos para presentación. Se contará con hasta 15 minutos para la presentación y hasta 7 minutos para preguntas y retroalimentación.</li>
</ul>


<h4>IV. Participación de estudiantes</h4>

Todos los estudiantes de Maestría o Doctorado, en programas afines con la Informática y la Computación, están cordialmente invitados a someter sus proyectos a evaluación, para ser presentados en el Simposio. Para tal efecto, se debe, a más tardar hasta el <span class="tutTitle">domingo  23 de Agosto</span>, enviar al correo <span class="tutTitle">simposio@sco2.org</span>, empleando el asunto <span class="tutTitle">SIMPOSIO M&D 10CCC</span>, un archivo PDF que siguiendo el mismo formato de envío de artículos del 10CCC (i.e. plantilla IEEE Conference), y en una extensión máxima de dos (2) páginas (i.e. una hoja por lado y lado), presente y describa el proyecto cumpliendo las siguientes directrices:<br/>
<ul>
<li>Título, autores, afiliación, resumen, descripción y referencias. Se consideran como autores, tanto al estudiante de posgrado, como a su(s) respectivo(s) tutor(es). La descripción debe incluir una introducción, al problema abordado, y/o a la pregunta de investigación, los objetivos y alcances planteados, e indicar si corresponde a una investigación a nivel de Maestría o de Doctorado. </li><li>Detallar el estado de avance del proyecto, incluyendo en las referencias las publicaciones que se hayan obtenido hasta el momento.</li><li>Describir el  aporte que el proyecto brinda a la solución de problemáticas, tanto a nivel nacional, como internacional.
</li>
</ul>No se requiere que en el proyecto ya se esté logrando el impacto esperado, pero la descripción del mismo debe reflejar una reflexión juiciosa al respecto. Así pues, se les recuerda a los estudiantes que el Simposio busca motivarlos a que se cuestionen sobre cómo su trabajo podría aportar en forma inmediata o futura al desarrollo de la sociedad, ojala haciendo énfasis en su entorno inmediato: Colombia.</br>Los resúmenes recibidos serán evaluados con la ayuda del Comité Directivo de la Sociedad Colombiana de Computación – SCo2. Los evaluadores considerarán sobre cada proyecto, su  contribución a la Informática y la Computación, su impacto sobre la problemática abordada, y la calidad de la redacción y estructuración del documento. El <span class="tutTitle">miércoles 26 de Agosto</span> se anunciará qué trabajos han sido seleccionados. Se asume que el estudiante que somete su trabajo al Simposio, realizará la presentación del mismo, en caso de ser seleccionado. <span class="tutTitle">La participación en la actividad del Simposio no tendrá costo para los estudiantes cuyos proyectos hayan sido seleccionados para presentación oral.</span><br/>Si bien el Simposio no está orientado a publicación de artículos, se espera poder generar unas memorias del mismo que se difundirán a través de los canales de la SCo2. Adicionalmente, la SCo2 entregará una mención especial al estudiante que realice la mejor presentación durante el Simposio, en cada una de las dos categorías contempladas.
<br/>


<h4>V. Participación de profesores</h4>

Todos los profesores participantes en programas de Maestría o Doctorado, afines con Informática y Computación, están cordialmente invitados a asistir y participar en el Simposio, sin ningún costo. En primera instancia, se espera que los profesores realicen aportes constructivos a los estudiantes que presentarán sus trabajos. Se espera poder contar con la participación de al menos un profesor por cada una de las instituciones que ya tienen en funcionamiento un programa de Maestría y/o Doctorado en áreas afines con Informática y Computación.

<h4>VI. Participación de socios de la SCO2 y de Inscritos al 10CCC</h4>

Todos los socios que se encuentren al día en sus respectivos pagos de membresía a la SCo2, así como las personas previamente inscritas al 10CCC, podrán asistir al evento del Simposio. Su participación durante las sesiones de retroalimentación, estará sujeta a la calidad de ostentar el título de Maestría o Doctorado. <br/>

Adicionalmente, los estudiantes que hayan realizado presentación oral en el Simposio, y que se encuentren inscritos al 10CCC podrán preparar un poster para ser expuesto durante el <span class="tutTitle">Evento de Posters del miércoles 23 de Septiembre</span>, en el marco del Congreso. De esta forma se extiende la divulgación de su trabajo, y la posibilidad de recibir retroalimentación.


<h4>VII. Contacto</h4>

Preguntas o inquietudes sobre la presente convocatoria, o sobre el Simposio, se deben dirigir a al correo simposio@sco2.org, empleando el asunto <span class="tutTitle">CONSULTA SIMPOSIO</span>. Dichos correos se recibirán y atenderán hasta el día viernes 21 de Agosto. 





