<h3>Memorias</h3>
 
Las memorias del evento serán sometidas para publicación, después del 10CCC, en IEEEXplore. Sólo son elegibles para ser publicados los artículos que hayan sido presentados en la conferencia por uno de sus autores. Además, se espera publicar versiones extendidas de una selección de artículos completos distinguidos en la Revista Colombiana de Computación (RCC) y en la revista Avances en Sistemas e Informática.
