<h3>Llamado de trabajos</h3>

<p>		
La Sociedad Colombiana de Computación (SCo2) y la Universidad de los Andes los invitan a enviar trabajos para la edición 10 del Congreso Colombiano de Computación. El 10CCC se llevará a cabo en Bogotá entre el 21 y 25 de septiembre y espera recibir trabajos que presenten avances en (i) las diferentes líneas o (ii) la aplicación y práctica de tecnologías novedosas de la informática. Además de artículos completos, son bienvenidos posters en todas las líneas que presenten avances preliminares y para los cuales el 10CCC sea una valiosa oportunidad de discusión.
</p>		
<p>
Los trabajos pueden ser presentados en castellano o en inglés. Los artículos completos deberán seguir el formato IEEE y no exceder 8 páginas tamaño carta. Se recibirán trabajos en formatos LaTeX (se prefiere esta opción) o Word. Los autores deberán utilizar las plantillas que se encuentran publicadas en:
</p>		
<p> 
<a target="_blank" href="http://www.ieee.org/conferences_events/conferences/publishing/templates.html">http://www.ieee.org/conferences_events/conferences/publishing/templates.html</a>
</p>		
<p> 
El envío de trabajos debe hacerse electrónicamente vía EasyChair:
</p>		
<p>
<a target="_blank" href="https://easychair.org/conferences/?conf=10ccc">https://easychair.org/conferences/?conf=10ccc</a>
</p>		
