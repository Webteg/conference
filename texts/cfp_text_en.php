<h3>Proceedings</h3>
 
Negotiations have been made to make the post-proceedings of the conference available through IEEEXplore. Only papers presented at the conference by their authors can be published in IEEEXplore. Furthermore, the best papers from the conference will be sent to RCC and Avances en Sistemas e Informática for the publication of improved and/or extended versions.
