<h3>Call for papers</h3>

<p>		
The Colombian Computing Society (SCo2) and Universidad de los Andes invite you to submit unpublished papers with original work to the 10th edition of the Colombian Computing Congress (10CCC) that will take place in Bogotá, Colombia. Submitted works are expected to present advances in different disciplines related to computing, as well as practical works showing the application of novel techniques in real scenarios. Besides full papers, the 10CCC also welcomes poster submissions presenting works in their early stages of development.
</p>		
<p>
Submissions are acceptable written in Spanish or English. Full papers should be formatted according to IEEE guidelines and they should not exceed 8 letter-size pages. LaTeX and MS Word type settings are acceptable (although LaTeX is preferred). Authors are strongly recommended to use the templates which are available at:
</p>		
<p> 
<a target="_blank" href="http://www.ieee.org/conferences_events/conferences/publishing/templates.html">http://www.ieee.org/conferences_events/conferences/publishing/templates.html</a>
</p>		
<p> 
Work submission has to be done electronically via EasyChair:
</p>		
<p>
<a target="_blank" href="https://easychair.org/conferences/?conf=10ccc">https://easychair.org/conferences/?conf=10ccc</a>
</p>		
