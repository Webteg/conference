<h4>Inscripción de conferencistas</h4>

<p>
Por cada artículo que sea presentado en la conferencia e incluido en las memorias del evento debe haber por lo menos un autor registrado como <span style="font-weight: bold;">conferencista</span>, independientemente de si se trata de un estudiante, profesional o miembro de una sociedad.
</p>


<h4>Pagos institucionales</h4>

<p>
Para pagos institucionales</span>, la institución debe enviar una carta de compromiso a 
  <a href="mailto:10ccc@sco2.org?cc=mh.rodriguez@uniandes.edu.co">10ccc@sco2.org</a> 
con copia a <a href="mailto:10ccc@sco2.org?cc=mh.rodriguez@uniandes.edu.co">mh.rodriguez@uniandes.edu.co</a> 
solicitando a la Universidad de los Andes la generación de la factura a nombre de la institución correspondiente. La carta de compromiso debe incluir la información de registro para los asistentes. Puede utilizar <a href="CartaCompromiso10CCC.docx" target="_blank">este formato</a> para la carta.
</p>

<h4>Descuentos para grupos</h4>
<p>
Hasta el 1ero de Septiembre aplican descuentos para grupos: por cada 5 personas inscritas de una misma institución puede asistir una persona adicional a la conferencia.
</p>
<p> <i>Condiciones</i>: para cada participante aplica la tarifa que le correspondería si no hiciera parte del grupo; el participante adicional debe ser al que le corresponda la tarifa de menor valor.
</p>
<p> <i>Nota</i>: es indispensable enviar la información de registro de la persona adicional.
</p>

