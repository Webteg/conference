<h3>Envíos aceptados y condicionales</h3>

<p>Como respuesta al llamado de trabajos del 10 CCC, se recibieron artículos provenientes de 22 países, incluyendo Colombia, Ecuador, Panamá, España, Brasil, USA y Chile.</p>
<p>Todos los artículos que se recibieron fueron evaluados por al menos 2 revisores expertos en el tema y luego el comité de programa de cada línea tomó la decisión de aceptar o no cada envío teniendo en cuenta los comentarios y el nivel de experticia de los revisores. Además, para algunos artículos se solicitó el envío de una versión revisada y mejorada en la que se atendieran importantes solicitudes de los revisores.</p>
<p>A continuación se encuentra la lista de artículos que fueron aceptados (esta lista incluye los aceptados condicionalmente).</p>


