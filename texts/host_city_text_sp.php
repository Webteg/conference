<p>		
Bogotá, the capital of Colombia, is located 2600 meters above sea level (8660 feet) in the Andes. 
It is an enormous city full of contrasts: while downtown Bogotá looks almost the same as it looked 150 years ago, other areas
are completely modern and look similar to any modern capital in the world. With approximately 7 million inhabitants, Bogotá 
is a city in continous transformation and the largest contributor to the Colombian GDP.
</p>
<p>
Without doubt, Bogota's most salient feature are the Andes mountains that guard it on the east. Among those, the most 
representative is the mountain of Monserrate, where the monastery of the same name resides. Located 500 meters above the
city center, a trip to Monserrate offers breath taking views of the city and allows visitors to realize the magnitude
of the city. 
</p>

