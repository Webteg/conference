<h4>1. Introducción al modelo de actores con Akka.</h4>
	<div>
	<span class="tutTitle">Descripción:</span> 
	<span class="tutValue">Los requerimientos de las aplicaciones de software empresarial han cambiado de manera dramática. Solo hace unos años, las grandes aplicaciones contaban con decenas de servidores, segundos en los tiempos de respuesta, horas de mantenimiento fuera de línea y gigas de datos. Hoy, las aplicaciones empresariales atienden clientes distribuidos geográficamente. Estas  aplicaciones son desplegadas en clusters basados en nubes y son ejecutadas en procesadores de múltiples cores con gigas de memoria disponible. Los usuarios esperan respuestas en mili segundos y una disponibilidad del 100%. Los datos son fácilmente medidos en Petabytes. <br/>

Las demandas de las aplicaciones actuales necesitan nuevos acercamientos arquitectónicos y nuevas tecnologías. Dentro de estas nuevas tecnologías se encuentra Akka. Akka es un toolkit y runtime para la construcción de aplicaciones altamente concurrentes, distribuidas, resilientes y basadas en el intercambio de mensajes sobre la JVM.  <br/>

El tutorial tiene como objetivo introducir a los asistentes a esta nueva forma de construir aplicaciones empresariales haciendo uso de Akka. El tutorial es completamente práctico.</span>
