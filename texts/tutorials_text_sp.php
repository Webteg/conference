<h4>4. Modelado de Sistemas Auto-Adaptables.</h4>

	<div>
	<span class="tutTitle">Descripción:</span> 
	<span class="tutValue">EEste tutorial introduce los sistemas auto-adaptables, un lenguaje para el modelamiento de sus requerimientos particulares (REFAS) y una herramienta que soporta el lenguaje (VariaMos). El propósito principal del tutorial es ayudar a entender las características del modelamiento y simulación de requerimientos de sistemas auto-adaptables mediante la herramienta VariaMos, y mostrar la capactidad de extensibilidad de la misma a modelos diferentes a los predefinidos en REFAS, incluyendo su sintaxis abstracta y concreta, y respectiva semántica operacional. <br/>

