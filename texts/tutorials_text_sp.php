<h4>5. Un cuento de Árboles, Grafos, y Redes: Técnicas y Herramientas de visualización y análisis.</h4>

	<div>
	<span class="tutTitle">Descripción:</span> 
	<span class="tutValue">
Desde el principio, nuestra formación ha sido acompañada por la trinidad del árbol, el grafo, y la red. Vimos árboles en estructuras de directorios, índices de bases de datos, árboles de decisión, y jerarquías organizacionales. Por otro lado, vimos grafos en sus diferentes presentaciones: en su forma matemática, en máquinas de estado, diagramas, procesos de negocio, e incluso pudimos ver que la estructura de modelos, ontologías, y esquemas de bases de datos son básicamente grafos. <br/>

Luego llegaron las redes: Internet y topologías de redes, infraestructuras tecnológicas, Redes Neuronales, redes de distribución, Redes de Petri, Arquitecturas (de Software, de Negocio, Empresariales), o en general, Redes Complejas. De la misma forma, hemos pasado de analizar propiedades, a ejecutar algoritmos, a crear protocolos. Estas estructuras han permeado el lenguaje actual, y se han extendido más allá de nuestros respectivos campos. Ahora no podemos evitar ver todo conectado: Redes Sociales, redes de conocimiento, redes terroristas, redes de contagio, movilizaciones de masas, redes económicas y financieras...  <br/>

Sin embargo, cada vez es más difícil visualizar, explorar, y aprender de estas estructuras: muchas de las redes que usamos hoy en día simplemente no caben en nuestras cabezas. En los últimos años hemos visto una explosión de herramientas de software que nos permiten manejar altos volúmenes de información, y razonar visualmente con los datos. También somos testigos del nacimiento de disciplinas como la Analítica Visual, junto con una demanda laboral cada vez mayor de Arquitectos, Analistas, y Científicos de Datos, o en general, personas que puedan superar la complejidad implícita de estas redes en sus respectivos dominios. Por estas razones, es necesario tener habilidades, al menos básicas, para visualizar y analizar información de carácter jerárquico, de grafo, o de red.  <br/>

Este taller intenta ser gráfico e interactivo, y se divide en tres partes: Primero se realizará un recorrido por estas tres estructuras, y examinaremos por qué y cómo las visualizamos hoy en día. En la segunda parte, los participantes podrán interactuar con las diferentes técnicas y herramientas para su visualización y análisis, haciendo énfasis en varios frameworks y librerías en JavaScript. La tercera parte será un tutorial práctico para desarrollar visualizaciones interactivas de conjuntos de datos de diferentes dominios: jerárquicos -el modelo de una estructura organizacional-, grafos -de diferentes clases-, y redes complejas -una arquitectura empresarial-.
